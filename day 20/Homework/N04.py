sentance = input("enter a sentance" )
vowel_count=0
for char in sentance:
    if char == "a" or char == "A":
        vowel_count += 1
    elif char == "e" or char == "E":
        vowel_count += 1
    elif char == "i" or char == "I":
        vowel_count += 1
    elif char == "o" or char == "O":
        vowel_count += 1
    elif char == "u" or char == "U":
        vowel_count += 1